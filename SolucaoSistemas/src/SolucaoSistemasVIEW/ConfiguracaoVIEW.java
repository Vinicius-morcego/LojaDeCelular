    private void ConsultarLogo(){
        try {
            ResultSet logo = null;
            logo = objconfigurarCTR.ConsultarLogoCTR();
            if(logo.next()){
                UtilVIEW.CarregaLabel(lblRelatorio, logo, "logotipo");
                txtNomeFantasia.setText(logo.getString("titulo"));
                txtTelefone1.setText(logo.getString("telefone1"));
                txtTelefone2.setText(logo.getString("telefone2"));
                
            }
        } catch (Exception e) {
            System.out.println(""+e.getMessage());
        }
    }
    private void SalvarConfiguracao(){
        try {
            if(objconfigurarCTR.CadastrarConfiguracaoCTR(txtNomeFantasia.getText().toUpperCase(), 
                    txtTelefone1.getText(), txtTelefone2.getText(), lblRelatorio.getText())){
                JOptionPane.showMessageDialog(null, "Salvo com sucesso!", "MENSAGEM", 
                        JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar!", "MENSAGEM", 
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }
    private void AlterarImagemRelatorio(){
        try {
            objconfigurarCTR.AlterarImagemRelatorioCTR(lblRelatorio.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao Alterar Logotipo", "MENSAGEM", 
                    JOptionPane.WARNING_MESSAGE);
        }
    }
    private void AtualizarConfiguracao(){
        try {
            if(objconfigurarCTR.AlterarConfiguracaoCTR(txtNomeFantasia.getText().toUpperCase(), 
                    txtTelefone1.getText(), txtTelefone2.getText())){
                JOptionPane.showMessageDialog(null, "Atualizado com sucesso!", "MENSAGEM", 
                        JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao atualizar!", "MENSAGEM", 
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }
