    private void CadastrarUsuarioVIEW(){
        try {
            UsuarioCTR objusuarioCTR = new UsuarioCTR();
            char[] pegaSenha;
            String senha = "";
            pegaSenha = txtSenha.getPassword();
            senha = String.valueOf(pegaSenha);
            if(objusuarioCTR.CadastrarUsuarioCTR(String.valueOf(cbxPessoa.getSelectedItem()), 
                    txtUsuario.getText(), senha)){
                JOptionPane.showMessageDialog(null, "Salvo com sucesso!", "MENSAGEM", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar!", "MENSAGEM", JOptionPane.WARNING_MESSAGE);
        }
    }
    
    private void ConsultarUsuarioVIEW(){
        try {
            UsuarioCTR objusuarioCTR = new UsuarioCTR();
            ResultSet user = null;
            user = objusuarioCTR.ConsultarUsuarioCTR();
            RestaurarUsuario(user);
        } catch (Exception e) {
        }
    }
    
    private void RestaurarUsuario(ResultSet rs){
        try {
            DefaultTableModel dtm = (DefaultTableModel) tblUsuario.getModel();
            dtm.setRowCount(0);
            while(rs.next()){
                dtm.addRow(new Object[]{rs.getString("codigo"), rs.getString("loguin")});
            }
            tblUsuario.setModel(dtm);
        } catch (Exception e) {
        }
    }
