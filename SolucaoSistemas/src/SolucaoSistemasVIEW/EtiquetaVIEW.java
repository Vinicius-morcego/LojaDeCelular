    private void gerarEtiqueta() {
        try {
            URL arquivo = getClass().getResource("/Relatorios/etiquetas.jasper");
            Map parametros = new HashMap();
            
            for (int i = 0; i < barraspegar.size(); i++) {
                objProduto = new ProdutoServicoDAO();
                objProduto.setNome(String.valueOf(barraspegar.get(i)));
                objProduto.setCodigobarras(String.valueOf(valores.get(i)));
                nomes.add(objProduto);
            }
            JasperReport jr;
            jr = (JasperReport) JRLoader.loadObject(arquivo);
            JasperPrint print = null;
            print = JasperFillManager.fillReport(jr, parametros, new JRBeanCollectionDataSource(nomes));
            //JasperViewer view = new JasperViewer(print, false);
            JasperExportManager.exportReportToPdfFile(print, "/SolucaoSistemas/etiqueta.pdf");
            java.awt.Desktop.getDesktop().open(new File("/SolucaoSistemas/etiqueta.pdf"));
            //view.setVisible(true);
            nomes.clear();
        } catch (JRException | IOException ex) {
            System.out.println("" + ex.getMessage());
        }
    }

    private void ConsultarProdutos() {
        try {
            ProdutoServicoCTR objprodutoCTR = new ProdutoServicoCTR();
            ResultSet prod = null;
            dl = new DefaultListModel();
            dl.clear();
            prod = objprodutoCTR.GerarCodigoBarrasCTR(txtEtiquetas.getText().toUpperCase());
            prod.beforeFirst();
            while (prod.next()) {
                dl.addElement(prod.getString("nome"));
            }
            listaProdutos.setModel(dl);
           // dl.clear();
        } catch (SQLException e) {
            System.out.println("" + e.getMessage());
        }
    }

