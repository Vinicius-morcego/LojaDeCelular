    private void btnAdicionaListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionaListaActionPerformed
        try {
            dl = (DefaultListModel<String>) listaPreenche.getModel();
        } catch (Exception e) {
            dl = new DefaultListModel<>();

        }

        try {
            valor = listaProdutos.getSelectedValue();
            dl.addElement(valor);
            listaPreenche.setModel(dl);
            objProduto.setNome(String.valueOf(valor));
            objProduto.setReferencia("null");
            objProduto.setCodigobarras("null");
            objProduto.setCodigo("1");
            objProduto.setMargem("0");

            consultaValor = objProduto.ConsultarProdutoDAO();
            
            if (consultaValor.next()) {
                barraspegar.add(consultaValor.getString("nome"));
                valores.add(consultaValor.getString("codigobarras"));
            }
            valor = "";
            consultaValor = null;
            listaProdutos.clearSelection();
            txtEtiquetas.requestFocusInWindow();
        } catch (SQLException | ClassCastException e) {
            System.out.println("AQUI " + e.getMessage());
        }
    }//GEN-LAST:event_btnAdicionaListaActionPerformed
    private void btnRemoveListaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveListaActionPerformed

        try {

            valor = listaPreenche.getSelectedValue();
            indice = listaPreenche.getSelectedIndex();
            dl = (DefaultListModel) listaPreenche.getModel();
            dl.removeElement(valor);
            barraspegar.remove(barraspegar.get(indice));
            valores.remove(valores.get(indice));
            listaPreenche.setModel(dl);
            txtEtiquetas.requestFocusInWindow();
        } catch (ArrayIndexOutOfBoundsException e) {
            System.out.println("" + e.getMessage());
        }


    }//GEN-LAST:event_btnRemoveListaActionPerformed

