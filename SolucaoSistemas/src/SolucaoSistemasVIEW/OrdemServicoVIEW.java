    private void CadastrarOrdemServico() {
        try {
            Date data = new SimpleDateFormat("dd/MM/yyyy").parse(txtData.getText());
            objordemCTR.CadastrarOrdemServicoCTR(codigoCliente, new SimpleDateFormat("yyyy-MM-dd").format(data),
                    txtDescricao.getText(), "Pendente", txtModelo.getText().toUpperCase(),
                    txtMarca.getText().toUpperCase(), txtCor.getText().toUpperCase(),
                    txtNumeroSerie.getText().toUpperCase());
            JOptionPane.showMessageDialog(null, "Salvo com sucesso!", "MENSAGEM", JOptionPane.INFORMATION_MESSAGE);
        } catch (ParseException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar!", "MENSAGEM", JOptionPane.WARNING_MESSAGE);
            System.out.println("" + e.getMessage());
        }
    }

    private void ConsultarCodigoOrdemServico() {
        try {
            ResultSet rs;
            rs = objordemCTR.ConsultarMaiorCodigoOrdemServicoCTR();
            if (rs.next()) {
                txtCodigo.setText(rs.getString(1));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao ConsularCodigoOrdemServico", "MENSAGEM", JOptionPane.WARNING_MESSAGE);
            System.out.println("" + e.getMessage());
        }
    }

