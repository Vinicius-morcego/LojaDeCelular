    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            if (txtCliente.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Preencher o campo cliente!", "MENSAGEM",
                        JOptionPane.WARNING_MESSAGE);
                txtCliente.requestFocusInWindow();
            } else if (txtData.getText().equals("  /  /    ")) {
                JOptionPane.showMessageDialog(null, "Preencher o campo data!", "MENSAGEM",
                        JOptionPane.WARNING_MESSAGE);
                txtData.requestFocusInWindow();
            } else if (txtDescricao.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Preencher o campo descrição!", "MENSAGEM",
                        JOptionPane.WARNING_MESSAGE);
                txtDescricao.requestFocusInWindow();
            } else {

                UtilVIEW objutil = new UtilVIEW();
                CadastrarOrdemServico();
                this.dispose();
                objutil.ImprimirOrdemServico(txtCodigo.getText(), txtTotal.getText());

            }
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(null, "Erro ao Salvar!", "MENSAGEM", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

