    private void CadastrarAgendaVIEW() {
        try {
            if (codigoAnimal.equals("")) {
                codigoAnimal = "0";
            } else {

            }
            if (objagendaCTR.CadastrarAgendaCTR(dataFormatar.format(txtAgendar.getDate()),
                    txtHora.getText(), txtDiaSemana.getText(), situacao,
                    txtCliente.getText(), codigoAnimal, txtTelefone1.getText(), txtTelefone2.getText(),
                    String.valueOf(cbxAtendente.getSelectedItem()),
                    String.valueOf(cbxVeterinario.getSelectedItem()),
                    txtObservacao.getText().toUpperCase())) {
                JOptionPane.showMessageDialog(null, "Salvo com sucesso!", "MENSAGEM",
                        JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar", "MENSAGEM",
                    JOptionPane.WARNING_MESSAGE);
        }
    }

    private void AlterarAgendaVIEW() {
        try {

            if (objagendaCTR.AlterarAgendaCTR(dataFormatar.format(txtAgendar.getDate()),
                    txtHora.getText(), txtDiaSemana.getText(), situacao,
                    txtCliente.getText(), codigoAnimal, txtTelefone1.getText(), txtTelefone2.getText(),
                    String.valueOf(cbxAtendente.getSelectedItem()),
                    String.valueOf(cbxVeterinario.getSelectedItem()),
                    txtObservacao.getText().toUpperCase(), PesquisaAgendamentoVIEW.codigoAgenda)) {
                JOptionPane.showMessageDialog(null, "Agendado com sucesso!", "MENSAGEM",
                        JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao agendar", "MENSAGEM",
                    JOptionPane.WARNING_MESSAGE);
        }
    }

    private void RestaurarAgenda() {
        try {
            ResultSet restaura = null;
            restaura = objagendaCTR.ConsultarAgendaCTR(PesquisaAgendamentoVIEW.codigoAgenda);
            if (restaura.next()) {
                txtAgendar.setDate(restaura.getDate("datas"));
                txtHora.setText(restaura.getString("hora"));
                txtDiaSemana.setText(restaura.getString("dia"));
                txtCliente.setText(restaura.getString("cliente"));
                txtNomeAnimal.setText(restaura.getString("nome"));
                codigoAnimal = restaura.getString("animal");
                txtTelefone1.setText(restaura.getString("telefone1"));
                txtTelefone2.setText(restaura.getString("telefone2"));
                cbxAtendente.removeAllItems();
                cbxAtendente.addItem(restaura.getString("atendente"));
                cbxVeterinario.removeAllItems();
                cbxVeterinario.addItem(restaura.getString("veterinario"));
                txtObservacao.setText(restaura.getString("observacao"));
                if (restaura.getString("situacao").equals("DISPONIVEL")) {
                    rbDisponivel.setSelected(true);
                } else if (restaura.getString("situacao").equals("CONFIRMADO")) {
                    rbConfirmado.setSelected(true);
                } else if (restaura.getString("situacao").equals("CONFIRMAR")) {
                    rbAConfirmar.setSelected(true);
                } else if (restaura.getString("situacao").equals("CANCELADO")) {
                    rbCancelado.setSelected(true);
                }
            }
        } catch (Exception e) {
            System.out.println("" + e.getMessage());
        }
    }

