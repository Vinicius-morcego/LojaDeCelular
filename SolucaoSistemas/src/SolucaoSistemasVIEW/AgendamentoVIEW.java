    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            if (rbDisponivel.isSelected() == true) {
                situacao = "DISPONIVEL";
            } else if (rbConfirmado.isSelected() == true) {
                situacao = "CONFIRMADO";
            } else if (rbAConfirmar.isSelected() == true) {
                situacao = "CONFIRMAR";
            } else if (rbCancelado.isSelected() == true) {
                situacao = "CANCELADO";
            }
            if (PesquisaAgendamentoVIEW.codigoAgenda.equals("")) {
                CadastrarAgendaVIEW();
            } else {
                AlterarAgendaVIEW();
            }
            this.dispose();
            new PesquisaAgendamentoVIEW(null, true).setVisible(true);
        } catch (Exception e) {
            System.out.println("Botão Salvar " + e.getMessage());
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

