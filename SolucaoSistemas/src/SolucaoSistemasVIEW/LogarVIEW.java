    private void Logar() {
        try {
            if (cont == 3) {
                System.exit(0);
            } else {
                UsuarioCTR objusuarioCTR = new UsuarioCTR();
                String senha;
                char[] pegaSenha;
                ResultSet confere = null;
                pegaSenha = txtKey.getPassword();
                senha = String.valueOf(pegaSenha);
                confere = objusuarioCTR.ConsultarAcessoCTR(txtUser.getText(), UtilVIEW.Criptografar(senha));
                if (confere.next()) {
                    if (!txtUser.getText().equals(confere.getString("loguin"))
                            || !UtilVIEW.Criptografar(senha).equals(confere.getString("senha"))) {
                        JOptionPane.showMessageDialog(null, "Usuario ou senha não conferem! ",
                                "MENSAGEM", JOptionPane.WARNING_MESSAGE);
                        cont += 1;
                        txtUser.setText("");
                        txtKey.setText("");
                        txtUser.requestFocusInWindow();
                    } else {
                        this.dispose();
                        new PrincipalVIEW().setVisible(true);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Usuario ou senha não conferem!",
                            "MENSAGEM", JOptionPane.WARNING_MESSAGE);
                    cont += 1;
                    txtUser.setText("");
                    txtKey.setText("");
                    txtUser.requestFocusInWindow();
                }
            }

        } catch (SQLException | HeadlessException e) {
            JOptionPane.showMessageDialog(null, "Erro "+String.valueOf(e.getMessage()));
        }
    }

